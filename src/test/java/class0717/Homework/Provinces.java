public class Provinces extends Country{
    private String name;
    private String capital;

    public Provinces() {
    }

    public Provinces(String name, String provinces, String name1, String capital) {
        super(name, provinces);
        this.name = name1;
        this.capital = capital;
    }

    @Override
    public String getName() {
        return super.getName();
    }

    @Override
    public void setName(String name) {
        this.name = name;
    }

    public String getCapital() {
        return capital;
    }

    public void setCapital(String capital) {
        this.capital = capital;
    }

    @Override
    public String toString() {
        return "Provinces{" +
                "name='" + name + '\'' +
                ", capital='" + capital + '\'' +
                '}';
    }
}
